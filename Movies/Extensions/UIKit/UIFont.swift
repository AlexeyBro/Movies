import UIKit

extension UIFont {
    
    static var defaultBold24: UIFont {
        return UIFont.boldSystemFont(ofSize: 24)
    }
    
    static var defaultBold22: UIFont {
        return UIFont.boldSystemFont(ofSize: 22)
    }
    
    static var default20: UIFont {
        return UIFont.systemFont(ofSize: 20)
    }
    
    static var defaultBold18: UIFont {
        return UIFont.boldSystemFont(ofSize: 18)
    }
    
    static var default18: UIFont {
        return UIFont.systemFont(ofSize: 18)
    }
    
    static var default16: UIFont {
        return UIFont.systemFont(ofSize: 16)
    }
    
    static var default14: UIFont {
        return UIFont.systemFont(ofSize: 14)
    }
    
    static var defaultBold14: UIFont {
        return UIFont.boldSystemFont(ofSize: 14)
    }
}
